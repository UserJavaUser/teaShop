package by.htp.ishop.bean;

import java.io.Serializable;

public class CartItemView extends CartItem implements Serializable{

	private static final long serialVersionUID = -4403473554753897104L;

	private String name;
	private double price;

	public CartItemView() {
		
	}
	
	public CartItemView(CartItem cartItem, String name, double price) {
		super(cartItem.getId(), cartItem.getItemId(), cartItem.getQuantity(), cartItem.getCartId());
		this.name = name;
		this.price = price;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		long temp;
		temp = Double.doubleToLongBits(price);
		result = prime * result + (int) (temp ^ (temp >>> 32));
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!super.equals(obj))
			return false;
		if (getClass() != obj.getClass())
			return false;
		CartItemView other = (CartItemView) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (Double.doubleToLongBits(price) != Double.doubleToLongBits(other.price))
			return false;
		return true;
	}

}
